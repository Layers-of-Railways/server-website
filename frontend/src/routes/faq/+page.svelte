			<h2 id="nation-questions">Nation Questions</h2>
			<h3 id="what-s-a-nation-">What&#39;s a nation?</h3>
			<p>
				Nations are collections of players who have banded together for various reasons. There are
				benefits to being in a nation, such as funding for projects, easier access to materials, and
				so on. Nations contain guilds and businesses, and provide benefits to them while also
				benefiting from their trade.
			</p>
