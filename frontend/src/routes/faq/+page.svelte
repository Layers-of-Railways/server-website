			<h2 id="guild-questions">Guild Questions</h2>
			<h3 id="what-s-a-guild-">What&#39;s a guild?</h3>
			<p>
				A guild is an entity on the server that is either dedicated to producing a narrow range of
				products (potion, wood, stone) in bulk, or providing a specific service in bulk.
			</p>
			<h3 id="what-can-a-guild-do-">What can a guild do?</h3>
			<p>
				Guilds are generally allowed to: Make or harvest products designated as intermediates.
				Provide a generalized service (monster removal, train construction, bridge building, etc) to
				a nation as a whole.
			</p>
			<p>
				Intermediates are products that aren&#39;t meant to be directly consumed by a player, but
				can be used to make the goods that players consume. Some examples would be: flour, molten
				metals, bulk potion, raw vegetables or meat, cobblestone, logs and planks, lava, etc.
				Services that a guild provides should generally be contracted from a nation directly.
			</p>
			<h3 id="how-do-i-join-a-guild-">How do I join a guild?</h3>
			<p>Ask the members that comprise it.</p>
			<h3 id="can-i-make-my-own-guild-">Can I make my own guild?</h3>
			<p>
				You&#39;ll need to be part of a nation to make a guild. However, businesses are not bound by
				nations, and can be set up wherever you want, with slightly more restrictions.
			</p>
			<h2 id="business-questions">Business Questions</h2>
			<h3 id="what-s-a-business-">What&#39;s a business?</h3>
			<p>
				A business is an entity run by at least one person that sells a range of end products or
				specific services.
			</p>
			<h3 id="what-s-an-end-product-">What&#39;s an end product?</h3>
			<p>
				Anything a player would consume directly. This means food, tools, refined building blocks,
				bottled potions, redstone components, etc. There will be exceptions and nuance to this rule,
				as with all others, but generally speaking the rule is “if a player will use it directly,
				you can sell it.”
			</p>
